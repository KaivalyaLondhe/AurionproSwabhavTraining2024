package com.aurionpro.operators;

public class RelationalOperators {
	public static void main(String[] args) {
		//Relational operators check the relations within two variables
		int number1 = 10;
		int number2 = 20;
		System.out.println("The numbers " + number1 + " and " + number2 + " are equal. This statement is "+ (number1==number2));
		System.out.println("number1 != number2 : " + (number1!=number2));
		System.out.println("Is number1>number2? " + (number1>number2));
		System.out.println("Is number1<number2? " + (number1<number2));
	}
}
